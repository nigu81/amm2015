        <h2> Descrizione dell'applicazione </h2>
        <p>
            L’applicazione supporta la creazione e lavorazione di ordini online per pizzerie.
            Il cliente potrà scegliere in quale data vuole effettuare l'ordine e per quale pizzeria.
            Gli verrà data una lista di pizze da scegliere da aggiungere al suo ordine.
            Il gestore della pizzeria potrà visualizzare quali ordini sono stati commissionati
            alla sua pizzeria e procedere con la lavorazione e consegna dell'ordine.
        </p>
        

        <p>L’applicazione mantiene una anagrafica dei clienti e dei gestori, in particolare:</p>

        <ul>
            <li>Nome e Cognome</li>
            <li>Indirizzo</li>
            <li>Email</li>
        </ul>

        <h2> Il progetto soddisfa i seguenti requisiti </h2>
        <ul>
            <li>Utilizzo di HTML e CSS</li>
            <li>Utilizzo di PHP e MySQL</li>
            <li>Utilizzo del pattern MVC </li>
            <li>Due ruoli (cliente e gestore)</li>
            <li>Transazione per la eliminazione di un ordine (metodo eliminaOrdine della classe OrdineFactory.php)</li>
            

        </ul>
    </ul>

